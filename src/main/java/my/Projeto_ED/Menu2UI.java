    private void Remover_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Remover_btnActionPerformed
         if (tipoLista == "Lista Sequencial") {           
            int pos = Integer.parseInt(campo_pos.getText());
            
            if (pos >= 8){
                
             labelaviso.setText("Você digitou uma posição maior que o array");
             return;
                
            }
            
            if (pos > lista.tamanhoLista()){
                
                 labelaviso.setText("A posição está vazia!");
                 return;
                
            }
            
            lista.Remove(pos);
            int n = 0;
            int t = 0;
            while(t < 8){
                
                quadrados.get(t).setText(null);
                t++;
            }
            
            while (n < lista.tamanhoLista()) {
                String elemento = Integer.toString(lista.Elemento(n));
                
                quadrados.get(n).setText(elemento);
                n++;

            }
        } else {
            
            int pos = Integer.parseInt(campo_pos.getText());
            
            if (pos >= 8){
                
             labelaviso.setText("Você digitou uma posição maior que o array");
             return;
                
            }
            
            if (Integer.toString(listaEnc.Elemento(pos)) == null){
                
                 labelaviso.setText("A posição está vazia!");
                 return;
                
            }
            
            listaEnc.Remove(pos);
            int n = 0;
            int t = 0;
            while(t < 8){
                
                quadrados.get(t).setText(null);
                t++;
            }
            
            while (n < listaEnc.getTamanho()) {
                String elemento = Integer.toString(listaEnc.Elemento(n));
                quadrados.get(n).setText(elemento);
                n++;

            }
         }
    }//GEN-LAST:event_Remover_btnActionPerformed


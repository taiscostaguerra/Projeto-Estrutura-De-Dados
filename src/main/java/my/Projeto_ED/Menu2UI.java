    private void Adicionar_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Adicionar_btnActionPerformed

        if (tipoLista == "Lista Sequencial") {
 
            int elem = Integer.parseInt(campo_elem.getText());
            int pos = Integer.parseInt(campo_pos.getText());
            
            if (pos >= 8){
                
             labelaviso.setText("Você digitou uma posição maior que o array");
             return;
                
            }
            
           
            
            lista.Insere(pos, elem);
            int n = 0;
            int t = 0;
            
            while(t < 8){
                
                quadrados.get(t).setText(null);
                t++;
                
            }
           
            while (n < lista.tamanhoLista()) {
                String elemento = Integer.toString(lista.Elemento(n));
                quadrados.get(n).setText(elemento);
              
                n++;

            }
            
            resultpos.setText(null);
            
        } else {
            int j = 0;
            while(j < traco.size()){
                
                traco.get(j).setText("-->");
                j++;
                
            }

            int elem = Integer.parseInt(campo_elem.getText());
            int pos = Integer.parseInt(campo_pos.getText());
            
            if (pos >= 8){
                
             labelaviso.setText("Uma posição maior que a lista foi digitada!");
             return;
                
            }
            
            
            
            listaEnc.Insere(elem, pos);
            int n = 0;
            int t = 0;
            
            while(t < 8){
                
                quadrados.get(t).setText(null);
                t++;
                
            }
            
            while (n < listaEnc.getTamanho()) {
                String elemento = Integer.toString(listaEnc.Elemento(n));
                quadrados.get(n).setText(elemento);
               
                n++;

            }
        }

    }//GEN-LAST:event_Adicionar_btnActionPerformed

